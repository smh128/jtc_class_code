# to convert from fahrenheit to celsius, first subtract 32, then multiply by 5/9

celsius_100 = (100 - 32) * 5/9

print(celsius_100)

# convert a temperature of 0 degrees fahrenheit to celsius 

celsius_0 = (0 - 32) * 5/9
print(celsius_0)


# convert a temperature of 32.2 degrees fahrenheit to celsius 
celsius_34 = (32.2 - 32) * 5/9
print (celsius_34)

# convert a temperature of 5 degrees celsius to fahrenheit 

fahrenheit_5 = (5 * 9/5) + 32 
print (fahrenheit_5)

